package _4_class_and_object_in_java.bai_tap.xd_lop_Fan;

public class Fan {
    private int speed;
    private boolean on;
    private double radius;
    private String color;

    public int getSpeed() {
        return speed;
    }

    public void setSpeed() {
        this.speed = speed;
    }

    public boolean getOn() {
        return on;
    }

    public void setOn() {
        this.on = on;
    }

    public double getRadius() {
        return radius;
    }

    public void setRadius() {
        this.radius = radius;
    }

    public String getColor() {
        return color;
    }

    public void setColor() {
        this.color = color;
    }
}
